/// Buscar um estudante pelo nome e retornar a situação dele em cada matéria:
// Usando find e map;

const findStudent = (array, student) => {
  const studentFound = array.find((element) => element.nome === student);
  const studentMaterias = studentFound.materias;

  const situacaoMaterias = studentMaterias.map((element) => `Matéria: ${element.name} / Nota: ${element.nota} - ${element.nota >= 60 ? 'Aprovado' : 'Reporvado'} `);

  return situacaoMaterias;
};

